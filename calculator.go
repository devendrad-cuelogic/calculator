/*
Package calculator provides with basic arithmatic operations
like summation, substraction, multiplication and division.
*/
package calculator

/*
Sum method adds two numbers.
*/
func Sum(a int, b int) int {
	return a + b
}

/*
Sub method substracts two numbers.
*/
func Sub(a int, b int) int {
	return a - b
}

/*
Multiply method multiplies two numbers.
*/
func Multiply(a int, b int) int {
	return a * b
}

/*
Divide method divides two numbers.
*/
func Divide(a int, b int) int {
	return a / b
}
